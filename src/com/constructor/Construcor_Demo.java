package com.constructor;

public class Construcor_Demo {
	
	
	public Construcor_Demo(int a) {
		System.out.println(a);
	}
	public Construcor_Demo(String b) {
		System.out.println(b);
	}
	public Construcor_Demo(char c) {
		System.out.println(c);
	}
	public static void main(String[] args) {
		Construcor_Demo c1 = new Construcor_Demo("4");
		Construcor_Demo c2 = new Construcor_Demo("prasanth");
		Construcor_Demo c3 = new Construcor_Demo("j");
	}
	
	

}
